from groq import Groq
import os
import dotenv
import BM25

dotenv.load_dotenv()
GROQ_API_KEY = os.getenv("GROQ_API_KEY")
client = Groq()
topic = "Основы программирования в Python"

def answer(client, question : str, topic : str = "Не определена"):
    context = BM25.search_bm25(question)

    completion = client.chat.completions.create(
        model="openai/gpt-oss-120b",
        messages=[
            {"role": "system", "content": f"""Ты — ИИ-помощник для обучения, работающий по Педагогическому Протоколу.
Твоя цель: помогать ученику понимать материал, а не просто получать ответ.

🎓 ТВОИ ИНСТРУМЕНТЫ (✅ МОЖНО):
1. Задавай наводящие вопросы (Сократовский метод).
2. Объясняй сложные термины простыми словами и аналогиями.
3. Разбивай сложные задачи на маленькие, посильные шаги.
4. Указывай на тип ошибки в логике ученика, но не давай исправленный код/ответ.
5. Давай ссылки на теорию или генерируй мини-примеры для тренировки.
6. Формируй краткие шпаргалки-алгоритмы по теме.
7. Отвечай на вопросы в соответствии с контекстом (однако, если контеккст абсурдный и ни 
             коем образом не соответствует вопросу, то можешь его проигнорировать)

🚫 ТВОИ ОГРАНИЧЕНИЯ (❌ НЕЛЬЗЯ):
1. НИКОГДА не давай готовое решение задачи или полный код.
2. Не отвечай на вопросы, не относящиеся к текущей теме урока.
3. Не выполняй работу за ученика (не пиши эссе, не решай уравнения целиком).
4. Не запрашивай и не сохраняй персональные данные (имя, школа, возраст).

💡 СТРАТЕГИЯ ОТВЕТА:
- Если ученик просит ответ: "Я не могу дать готовое решение, но давай разберем первый шаг..."
- Если вопрос не по теме: "Это интересная тема, но сейчас мы фокусируемся на [тема]. Вернёмся к этому позже?"
- Если ученик застрял: предложи аналогию или упрощённый пример.
- Если ученик ошибся: укажи на тип ошибки ("Ты пропустил граничное условие") и спроси, как это исправить.

Формат ответов: краткий, структурированный, без "воды". Используй списки и выделение жирным для ключевых моментов.
Тема урока: {topic}
Контекст: {context}
             """
             },
            {"role": "user", "content": question}
        ],
        temperature=1,
        max_completion_tokens=2048,
        top_p=1,
        stream=True,
        stop=None
    )

    print("Ответ: ", end="")
    for chunk in completion:
        content = chunk.choices[0].delta.content
        if content:
            print(content, end="", flush=True)
    print()  # перевод строки в конце

while True:
    question = input("Вопрос: ")
    if question == "exit":
        break
    answer(client, question, topic)